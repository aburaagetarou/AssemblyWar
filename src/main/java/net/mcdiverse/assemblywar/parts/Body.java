package net.mcdiverse.assemblywar.parts;

import org.bukkit.Material;
import org.bukkit.World;
import org.bukkit.inventory.ItemStack;
import org.bukkit.inventory.meta.ItemMeta;

public class Body extends Parts {


	////////////////////////////////////////////////////////////
	// コンストラクタ
	////////////////////////////////////////////////////////////
	public Body(World world, int customModelData ) {

		// スーパークラスのコンストラクタを実行
		super( world );

		// カスタムモデルデータ付きのアイテムを生成
		ItemStack item = new ItemStack( Material.NETHERITE_AXE );
		ItemMeta meta = item.getItemMeta();
		meta.setCustomModelData( customModelData );
		item.setItemMeta( meta );

		// 初期設定
		this.init(item);
	}


	////////////////////////////////////////////////////////////
	// ポーズをセットする
	////////////////////////////////////////////////////////////
	private void pose() {}


	////////////////////////////////////////////////////////////
	// 前進時のポーズ
	////////////////////////////////////////////////////////////
	@Override
	protected void poseForward() {

		// ポーズセット
		pose();
	}


	////////////////////////////////////////////////////////////
	// 後進時のポーズ
	////////////////////////////////////////////////////////////
	@Override
	protected void poseBehind() {

		// ポーズセット
		pose();
	}


	////////////////////////////////////////////////////////////
	// 停止時のポーズ
	////////////////////////////////////////////////////////////
	@Override
	protected void poseStop() {

		// ポーズセット
		pose();
	}
}
